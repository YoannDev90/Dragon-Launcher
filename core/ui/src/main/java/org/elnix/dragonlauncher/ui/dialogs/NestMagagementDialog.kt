@Composable
fun NestManagementDialog(
    appsViewModel: AppsViewModel,
    title: String? = null,
    canCopyId: Boolean = true,
    onDismissRequest: () -> Unit,
    onNewNest: (() -> Unit)? = null,
    onNameChange: ((id: Int, name: String) -> Unit)?,
    onDelete: ((id: Int) -> Unit)?,
    onSelect: ((CircleNest) -> Unit)? = null
) {
    val ctx = LocalContext.current

    val points by SwipeSettingsStore.getPointsFlow(ctx).collectAsState(emptyList())
    val nests by SwipeSettingsStore.getNestsFlow(ctx).collectAsState(emptyList())

    val defaultPoint by appsViewModel.defaultPoint.collectAsState(defaultSwipePointsValues)

    val iconsShape by DrawerSettingsStore.iconsShape.flow(ctx)
        .collectAsState(DrawerSettingsStore.iconsShape.default)

    CustomAlertDialog(
        modifier = Modifier.padding(15.dp),
        onDismissRequest = onDismissRequest,
        alignment = Alignment.Center,
        scroll = false,
        confirmButton = {},
        title = {
            Text(
                text = title ?: stringResource(R.string.manage_nests),
                color = MaterialTheme.colorScheme.onSurface
            )
        },
        text = {
            LazyColumn(
                verticalArrangement = Arrangement.spacedBy(5.dp),
                modifier = Modifier.heightIn(max = 700.dp)
            ) {
                if (onNewNest != null) {
                    item {
                        Row(
                            modifier = Modifier
                                .fillMaxWidth()
                                .clip(CircleShape)
                                .clickable { onNewNest() }
                                .padding(5.dp),
                            verticalAlignment = Alignment.CenterVertically,
                            horizontalArrangement = Arrangement.Center
                        ) {

                            Icon(
                                imageVector = Icons.Default.AddCircle,
                                contentDescription = stringResource(R.string.create_new_nest),
                                tint = MaterialTheme.colorScheme.primary
                            )
                            Spacer(Modifier.width(15.dp))

                            Text(
                                text = stringResource(R.string.create_new_nest),
                                color = MaterialTheme.colorScheme.primary
                            )
                        }
                    }
                }

                items(nests) { nest ->
                    NestManagementItem(
                        nest = nest,
                        defaultPoint = defaultPoint,
                        canCopyId = canCopyId,
                        onNameChange = onNameChange,
                        onDelete = onDelete,
                        onSelect = { onSelect?.invoke(nest) }
                    )
                }
            }
        }
    )
}



@Composable
private fun NestManagementItem(
    nest: CircleNest,
    defaultPoint: SwipePointSerializable,
    canCopyId: Boolean,
    onNameChange: ((id: Int, name: String) -> Unit)?,
    onDelete: ((id: Int) -> Unit)?,
    onSelect: (() -> Unit)? = null
) {
    val ctx = LocalContext.current
    val extraColors = LocalExtraColors.current
    val density = LocalDensity.current
    val nests = LocalNests.current
    val points = LocalPoints.current
    val icons = LocalIcons.current
    val iconShape = LocalIconShape.current


    val maxNestsDepth by UiSettingsStore.maxNestsDepth.asState()

    var tempCustomName by remember { mutableStateOf(nest.name ?: "") }

    val surfaceColorDraw = MaterialTheme.colorScheme.surface.adjustBrightness(0.5f)

    val editPoint = SwipePointSerializable(
        circleNumber = 0,
        angleDeg = 0.0,
        SwipeActionSerializable.OpenCircleNest(nest.id),
        id = ""
    )

    Row(
        modifier = Modifier
            .fillMaxWidth()
            .height(100.dp)
            .clip(DragonShape)
            .background(surfaceColorDraw)
            .clickable { onSelect?.invoke() }
            .padding(5.dp),
        verticalAlignment = Alignment.CenterVertically
    ) {

        Canvas(
            modifier = Modifier
                .size(100.dp)
        ) {
            val center = size.center

            actionsInCircle(
                selected = false,
                point = editPoint,
                drawParams = SwipeDrawParams(
                    nests = nests,
                    points = points,
                    center =center,
                    ctx = ctx,
                    defaultPoint = defaultPoint,
                    icons = icons,
                    surfaceColorDraw = Color.Unspecified,
                    extraColors = extraColors,
                    showCircle = true,
                    density = density,
                    depth = 1,
                    maxDepth = maxNestsDepth,
                    iconShape = iconShape,100
                )
            )
        }

        Column(
            modifier = Modifier
                .weight(1f)
                .padding(10.dp)
        ) {

            Row(
                modifier = Modifier
                    .height(IntrinsicSize.Min)
                    .clickable(canCopyId) {
                        ctx.copyToClipboard(nest.id.toString())
                    },
                verticalAlignment = Alignment.CenterVertically,
                horizontalArrangement = Arrangement.spacedBy(5.dp)
            ) {
                Text(
                    text = "ID: ${nest.id}",
                    color = MaterialTheme.colorScheme.onSurface.copy(0.9f)
                )

                Icon(
                    imageVector = Icons.Default.ContentCopy,
                    contentDescription = stringResource(R.string.copy_id),
                    modifier = Modifier.size(18.dp)
                )
            }

            if (onNameChange != null) {
                TextField(
                    value = tempCustomName,
                    onValueChange = {
                        tempCustomName = it
                        onNameChange(nest.id, it)
                    },
                    placeholder = {
                        Text(
                            text = stringResource(R.string.custom_name),
                            color = MaterialTheme.colorScheme.onSurface
                        )
                    },
                    colors = AppObjectsColors.outlinedTextFieldColors(
                        backgroundColor = surfaceColorDraw,
                        onBackgroundColor = MaterialTheme.colorScheme.onSurface,
                        removeBorder = true
                    ),
                    singleLine = true,
                    modifier = Modifier
                        .weight(1f)
                )
            }
        }


        if (onDelete != null) {
            DragonIconButton(
                onClick = { onDelete(nest.id) }
            ) {
                Icon(
                    imageVector = Icons.Default.Close,
                    contentDescription = stringResource(R.string.delete_circle_nest),
                    tint = MaterialTheme.colorScheme.error
                )
            }
        }
    }
}
